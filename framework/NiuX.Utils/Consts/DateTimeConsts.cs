namespace NiuX.Consts;

/// <summary>
/// 日期
/// </summary>
public static class DateTimeConsts
{
    /// <summary>
    /// The common string
    /// </summary>
    public const string CommonString = "yyyy-MM-dd HH:mm:ss";

    /// <summary>
    /// The common date string
    /// </summary>
    public const string CommonDateString = "yyyy-MM-dd";

    /// <summary>
    /// The common time string
    /// </summary>
    public const string CommonTimeString = "HH:mm:ss";

    /// <summary>
    /// The common minute string
    /// </summary>
    public const string CommonMinuteString = "yyyy-MM-dd HH:mm";

    /// <summary>
    /// The common stamp string
    /// </summary>
    public const string CommonStampString = "yyyy-MM-dd HH:mm:ss.fff";

    /// <summary>
    /// The global minute string
    /// </summary>
    public const string GlobalMinuteString = "yyyy-MM-dd HH:mm zzz";

    /// <summary>
    /// The global stamp string
    /// </summary>
    public const string GlobalStampString = "yyyy-MM-dd HH:mm:ss.fff zzz";

    /// <summary>
    /// The global string
    /// </summary>
    public const string GlobalString = "yyyy-MM-dd HH:mm:ss zzz";
}